package Package1;

public class ForLoop_PrintEvenAndOddNumbers_0to100 
{

	public static void main(String[] args) 
	{
	System.out.println("Odd Numbers");
	for(int i=1;i<=100;i++)
	{
	System.out.println(i);
	i++;
	}
	System.out.println("Even Numbers");
    for(int i=2;i<=100;i++)
    {
     System.out.println(i);
     i++;
    }

}
}
